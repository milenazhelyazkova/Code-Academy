/*
Задача 17. Напишете програма за умножение на два двумерни
масиви, елемент по елемент.

*/
#include <stdio.h>

int main(){
   int i,j;
   int c[2][3];
   int a[2][3] = {1,2,3,4,5,6};
   int b[2][3] = {10,20,30,40,50,60};
   for (i=0; i<2; i++)
      for(j=0;j<3;j++)
         c[i][j] = a[i][j]*b[i][j];
   for(i=0;i<2;i++)
      for(j=0;j<3;j++){
         printf("%d  ", c[i][j]);
         if(j == 2)
            printf("\n");
      }
    return 0;
}