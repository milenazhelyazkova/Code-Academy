/*Задача 8. Използвайте функцията getchar(), с която въвеждате текст от клавиатурата,
 докато не подадете ЕОF(Ctrl+D). Текстът има букви и цифри. 
 Принтирайте с putchar(). Използвайте условните оператори, за да премахнете шпациите и табулациите в стринга*/

#include<stdio.h>

int main(){
    int c;
    while ((c= getchar())!= EOF){
        if(c==' ' || c == '\t')
            continue;   
        else
            putchar(c);
    }
}