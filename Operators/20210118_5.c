#include <stdio.h>
int main()
{
    int x = 1, y = 0;
    
    printf("\nInit values: X= %d, Y= %d\n", x, y);
    x = 1 + 2, 2 * 3, 3 + 4; // подедица от изрази щом не е в скоби и ще се присвои само x = 3
    y = (7 * 8, 8 + 9, 9 - 4);//в скоби е, съответно е последователност от изрази и се изчислява до последния x=9-4=5
    printf("\nX= %d, Y= %d\n", x, y);
    return 0;
}